// include the standard input/output functions - including printf
#include <stdio.h>

int main()
{
	// this is a variable named 'age' that is set to 10
	int age = 100;
	// this is a variable named 'height' that is set to 72
	int height = 72;
	// the printf function will then print out the age and height variables using format strings
	printf("I am %d years old. \n", age);
	printf("I am %d inches tall. \n", height);
	
	return 0;
}